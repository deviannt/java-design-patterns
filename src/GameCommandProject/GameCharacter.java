package GameCommandProject;

public class GameCharacter {
    public void moveForward() {
        System.out.println("Персонаж идёт вперёд");
    }

    public void moveBackward() {
        System.out.println("Персонаж отступает назад");
    }

    public void jump() {
        System.out.println("Персонаж прыгает");
    }

    public void useSkill() {
        System.out.println("Персонаж использует специальный навык!");
    }
}
